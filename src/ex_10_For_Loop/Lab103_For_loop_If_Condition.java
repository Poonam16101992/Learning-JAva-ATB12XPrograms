package ex_10_For_Loop;

public class Lab103_For_loop_If_Condition {
    public static void main(String[] args) {

        for( int age=0;age<20;age++){
            if(age>15) {
                System.out.println("Shruti will get gift after 15");
            }
            else{
                System.out.println("Shruti will not get any gift");

            }
        }
    }
}
