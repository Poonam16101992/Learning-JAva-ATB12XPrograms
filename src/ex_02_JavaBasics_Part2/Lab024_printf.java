package ex_02_JavaBasics_Part2;

public class Lab024_printf {
    public static void main(String[] args) {
        int a=10,b=20;
        System.out.printf("%d + %d",a,b);
        System.out.println();
        System.out.printf("a=%d  b=%d",a,b);
        //%d---> int
        //%f---> float,double
        //%s-->string
        //%b--> boolean
    }
}
