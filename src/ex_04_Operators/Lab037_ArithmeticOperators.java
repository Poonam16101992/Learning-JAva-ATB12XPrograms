package ex_04_Operators;

public class Lab037_ArithmeticOperators {
    public static void main(String[] args) {

        int a=20,b=10;
        float c= 3.14f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(c/a);
        System.out.println(a+c);
    }
}
